def get_movies(directory, movie_extensions=[MOV_EXT1, MOV_EXT2,
                                            MP4_EXT1, MP4_EXT2,
                                            AVI_EXT1, AVI_EXT2]):
    """
    Get the movies from the directory
    """
    movies = []
    for cur_format in movie_extensions:
        movies += glob.glob(os.path.join(directory, "*." +  cur_format))
    return movies


def get_start_end(folder, pattern="*.[0-9][0-9][0-9][0-9].*"):
    """
    Find start and end frames
    """
    files = glob.glob(os.path.join(folder, pattern))
    if files:
        files = sorted(files)
        start_file = files[0]
        end_file = files[-1]
        first_frame_number = int(os.path.split(start_file)[-1].split(".")[-2])
        last_frame_number = int(os.path.split(end_file)[-1].split(".")[-2])
        return {"start_frame" : first_frame_number, "end_frame" : last_frame_number}
    return None

def get_mask_frame_frames(file_pattern):
    """
    Add _mask as a suffix
    """
    parts = file_pattern.split(".")
    cstr = parts[0] + "_mask" + "." + parts[-2] + "." + parts[-1]
    hashed = parts[0] + "_mask" + "." + "####" + "." + parts[-1]
    return {"hash":hashed, "cstr":cstr}


